#include "detectionthread.h"

DetectionThread::DetectionThread(QObject *parent) : QThread (parent)
{
    cascadeName = "/usr/local/share/opencv4/haarcascades/haarcascade_frontalface_alt.xml";
    nestedCascadeName = "/usr/local/share/opencv4/haarcascades/haarcascade_eye_tree_eyeglasses.xml";
    scale = 1;
    camera = 0;
    tryflip = false;

    if (!nestedCascade.load(samples::findFileOrKeep(nestedCascadeName)))
        cerr << "WARNING: Could not load classifier cascade for nested objects" << endl;
    if (!cascade.load(samples::findFile(cascadeName)))
    {
        throw "ERROR: Could not load classifier cascade";
    }

    if(!capture.open(camera))
    {
        string err = string("Capture from camera #") + to_string(camera) + string(" didn't work");
        throw err;
    }
}

void DetectionThread::run()
{
    if( capture.isOpened() )
    {
        cout << "Video capturing has been started ..." << endl;
        while(1)
        {
            capture >> frame;
            if( frame.empty() ) {
                break;
            }
            Mat frame1 = frame.clone();
            detectAndDraw( frame1, cascade, nestedCascade, scale, tryflip );
        }
    }
}

